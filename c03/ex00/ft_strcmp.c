#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] == s2[i])
			i++;
		else
			return (s1[i] - s2[i]);
	}
	return (s1[i] - s2[i]);
}

int main(int argc, char **argv)
{
	int i;

	if (argc > 1)
	{	
		i = ft_strcmp (argv[1], argv[2]);
		printf("%d", i);
	}
}
